package com.example;

public class Player {
    private String name;
    private double attack;
    private double defense;
    private double health;
    private int level;

    public String getName() {
        return name;
    }

    public double getAttack() {
        return attack;
    }

    public double getDefense() {
        return defense;
    }

    public double getHealth() {
        return health;
    }

    public int getLevel() {
        return level;
    }
}
